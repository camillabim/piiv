        <h3>Bolo sem Lactose</h3>
        <h5>Ingredientes</h5>
        <h6><li>5 ovos</li>
        <li>1 lata de milho verde em conserva (sem o soro)</li>
        <li>1 pacote de coco relado 100 g</li>
        <li>1 lata de leite condensado</li>
        <li>1 colher de sopa de fermento para bolo</li>
        <h5>Modo de Preparo</h5></h6>
        <h6 class="text-justify">Misture tudo no liquidificador, deixando o fermento de lado.
            Bata até triturar tudo.
            Em seguida acrescente o fermento e misture.
            Unte uma forma redonda com furo no meio com óleo (apenas óleo).
            Leve ao forno em temperetura aproximadamente de 180º graus.</h6>
        </span>
    </div>

    <div class="border col-md-4 text-center">

        <span class="border-left">
        <img src="../images/Queijo-de-inhame-vegano.jpg" class="container-fluid rounded-circle" width="150px" height="150px">
        <h3>Queijo de Inhame Vegano</h3>
        <h5>Ingredientes</h5>
            <h6><li>½ de inhame cozido;</li>
                <li>½ cebola roxa;</li>
                <li>2 colheres (sopa) de Polvilho Azedo;</li>
                <li>5 colheres (sopa) de azeite;</li>
                <li>Sal e Ervas á gosto (experimente com o tempero hot, fica uma delícia)</li></h6>
            <h5>Modo de Preparo</h5>
        <h6 class="text-justify">	Bata a cebola e o inhame (após cozimento);
2	Adicione o Sal e as ervas de sua preferência (fica uma delicia com orégano) ;
3	Coloque no liquidificador 2 colheres do polvilho azedo e continue batendo;
4	Quando começar a empelotar coloque o óleo, aos poucos até ficar com uma textura de massa de modelar;
5	Coloque na geladeira em um pote até ficar rígido .</h6>
        </span>
    </div>


    <div class="border col-md-4 text-center">
        <img src="../images/palhaitaliana.jpg" class="container-fluid rounded-circle" width="150px" height="150px">
        <h3>Bolo sem Lactose</h3>
        <h5>Ingredientes</h5>
        <h6><li>2 ½ xícaras de alimento de amêndoas sabor chocolate Almond Breeze.</li>
        <li>½ xícara de açúcar demerara</li>
        <li>1 colher de chá de essência de baunilha</li>
        <li>1 ½ colher de sopa de cacau em pó dissolvidas em 3 colheres de sopa de água quente</li>
        <li>1 colher de chá de óleo</li>
            <li>1 colher de chá de amido dissolvido em 2 colheres de chá de água</li>
            <li>1 ½ xícaras de bolacha maisena picada</li>
            <li>¼ de xícara de amêndoas quebradas</li>
        <h5>Modo de Preparo</h5></h6>
        <h6 class="text-justify">Em uma panela dispor o alimento de amêndoas, o açúcar, a essência de baunilha, o cacau dissolvido e o óleo e mexer fogo médio por 5 minutos.

Juntar o amido dissolvido e cozinhar em fogo baixo por 45 minutos.

Quando começar a soltar do fundo da panela, juntar a bolacha e as amêndoas.

Colocar em uma forma untada com óleo e deixar na refrigeração por 4 horas

Cortar em quadradinhos e servir.

.</h6>

