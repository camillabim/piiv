<h2 class="text-center">Dicas para uma alimentação saudável</h2>

<div class="col-md-12">
    <div class="row">
        <div class="col-md-4 text-justify form-group border bg-success">
            <img src="../images/abacate-620x330.jpg" class="col-md-11 rounded-circle" width="300px">
            <br><br>
            <h5>Ter uma alimentação saudável é fundamental para que as funções do organismo funcionem de foma equilibrada.De forma prática,
            uma alimentação saudável é aquela composta por todos os macro e micronutrientes.
            Os macronutrientes são os carboidratos (pães, massas e batatas, entre outros), gorduras (como os óleos, as oleaginosas, abacate
            e outros) e proteínas (peixes, ovos, carnes vermelhas, carne de frango, entre outros). Enquanto os micronutrientes são as vitaminas
            e minerais, que estão presente nos mais diversos alimentos, como frutas, verduras, legumes, entre outros. As fibras,
            a parte não digerível do alimento vegetal, a qual resiste à digestão e à absorção intestinal, com fermentação completa
            ou parcial no intestino grosso, também são essenciais para a alimentação saudável e estão presentes nos alimentos integrais,
            nas frutas e verduras. Uma alimentação composta por estes nutrientes de forma equilibrada costuma ser bem variada, não tem
            exageros e não segue nenhum tipo de modismo.</h5>
        </div>
        <div class="col-md-4 text-justify form-group border bg-danger">
